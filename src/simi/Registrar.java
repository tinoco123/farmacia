    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (txtNombreUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Completa el campo nombre de producto");
        }
        if (txtContrasena.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Completa el campo cantidad de producto");
        }
        RepositorioAutenticacion repAutenticacion = new AutenticacionRepositorioImpl();
        if (repAutenticacion.nombreUsuarioExiste(txtNombreUsuario.getText())) {
            JOptionPane.showMessageDialog(this, "El nombre de usuario ya existe en el sistema.\n Ingresa otro nombre de usuario", "Nombre de usuario ocupado", JOptionPane.ERROR_MESSAGE);
        } else {
            Usuario usuario = new Usuario(txtNombreUsuario.getText(), txtContrasena.getText());
            if (repAutenticacion.registrar(usuario)) {
                JOptionPane.showMessageDialog(this, "El usuario se registro correctamente", "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                Farmacia farmacia = new Farmacia();
                farmacia.setVisible(true);
                farmacia.setLocationRelativeTo(this);
            } else {
                JOptionPane.showMessageDialog(this, "El usuario NO se registro correctamente", "Registro incompleto", JOptionPane.ERROR_MESSAGE);
                

            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

